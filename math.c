#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil function : %.2f",ceil(3.2));
	printf("\n ceil function : %.2f",ceil(3.9));
	
	printf("\n floor function : %.2f",floor(3.2));
	printf("\n floor function : %.2f",floor(3.9));
	
	printf("\n sqrt function : %.2f",sqrt(16));
	printf("\n sqrt function : %.2f",sqrt(7));
	
	printf("\n power function : %.2f",pow(2,3));
	printf("\n power function : %.2f",pow(2.2,3.2));
	
	printf("\n abs function : %.2f",abs(-12));
	getch();
}
